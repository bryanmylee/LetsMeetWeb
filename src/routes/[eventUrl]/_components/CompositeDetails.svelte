  import { layoutEnum, layout } from 'src/stores';
  import { detailsEnum, details } from '../stores';

  import EventDetails from './EventDetails.svelte';
  import FilterCard from './filterCard/FilterCard.svelte';

  // PROPS
  // =====
  export let event;
</script>

